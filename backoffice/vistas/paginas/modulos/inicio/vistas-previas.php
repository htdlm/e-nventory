    $qres = ControladorGeneral::ctrQuery("SELECT concat(year(fecha),\"-\",MONTH(fecha)) as periodo,round(sum(total),2) as ventas FROM `ventas` group by  periodo ");
 ?>

<script>

    var datos = <?php echo $qres ?>;
    var x = [];
    var y = [];

    for (var i in datos) {
        x.push(datos[i].periodo);
        y.push(datos[i].ventas);
    }

    var chartdata = {
        labels: x,
        datasets: [
            {
                label: 'Ventas por Mes',
                backgroundColor: '#49e2ff',
                borderColor: '#46d5f1',
                hoverBackgroundColor: '#CCCCCC',
                hoverBorderColor: '#666666',
                data: y
            }
        ]
    };

    var ctx = document.getElementById('salesChart');
    var barGraph = new Chart(ctx, {
        type: 'bar',
        data: chartdata
    });

</script>

